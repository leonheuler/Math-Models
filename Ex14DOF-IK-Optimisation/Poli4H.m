function ret = Poli4H(T0, T1, Y0, Y1, V0, V1, H, t)

    A = [T0^4           T0^3            T0^2            T0              1;
        T1^4            T1^3            T1^2            T1              1;
        ((T1+T0)/2)^4   ((T1+T0)/2)^3   ((T1+T0)/2)^2   ((T1+T0)/2)     1;
        4*T0^3          3*T0^2          2*T0            1               0;
        4*T1^3          3*T1^2          2*T1            1               0;];
    
    b = [Y0; Y1; Y0-H; V0; V1];
    
    a = A^(-1)*b;
    
    ret = a(1)*t^4 + a(2)*t^3 + a(3)*t^2 + a(4)*t + a(5);

end

